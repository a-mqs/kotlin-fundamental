package F_LacosDeRepeticao

fun main(){

    var i = -3
    var j = -3

    do {
        println("O valor do i no primeiro laço vale: $i")

        i += 2;

    } while (i <= 1)

    println()

    do {
        println("O valor do j no segundo laço vale: $j")

        j -= 2;

    } while (j >= 1)

}